        <h2 class="blog-post-title">Queimadas no Pantanal</h2> 
            
          <p class="blog-post-meta">Setembro 24, 2020 by <a href="#">Maria</a></p> <hr>

          <p>Segundo o <a href="https://g1.globo.com/natureza/noticia/2020/09/24/pantanal-bioma-mais-preservado-ate-2018-perdeu-ao-menos-10-vezes-mais-area-em-2020-que-em-18-anos.ghtml">G1</a>: </p>
          <p> Os incêndios que atingem o nosso Pantanal já consumiram dez vezes mais do que em 18 anos. O bioma já registra, este ano, o maior número de focos de incêndio da história.</p>

          <p>Dados divulgados nesta quinta-feira (24) pelo Instituto Brasileiro de Geografia e Estatística (IBGE) mostram que, entre 2000 e 2018, o Pantanal perdeu cerca de 2,1 mil km² de área nativa.</p>

          <p>Já em 2020, conforme os dados mais recentes divulgados por pesquisadores do Instituto Nacional de Pesquisas do Pantanal (INPP) e da Universidade Federal de Mato Grosso (UFMT), o bioma já viu cerca de 23 mil km² serem consumidos pelas chamas.</p>

          <p>A análise do IBGE considera apenas a área nativa perdida acumulada nos 18 anos de análise, sem diferenciar por qual meio se deu a perda. Já os dados no INPP e da UFMT consideram toda a área queimada, mesmo aquela que não é composta somente por vegetação natural.</p> 

