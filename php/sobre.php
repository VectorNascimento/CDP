		<h2 class="blog-post-title"> Primeiro de tudo: O que é ONG? Qual sua função?</h2>
	  
		<p>As <b>ONG's</b> são Organizações Não-Governamentais, criados pela sociedade civil para auxiliar o Estado na consecução de seu objetivo mais importante: 
		garantir o pleno exercício da cidadania e da democracia. Uma ONG é uma Organização Não-Governamental.
		</p>
	  
		<p>Sem fins lucrativos, cujo propósito é defender e promover uma causa política. Essa causa pode ser virtualmente de qualquer tipo: direitos humanos, 
		direitos animais, direitos indígenas, gênero, luta contra o racismo, meio ambiente, questões urbanas, imigrantes, entre muitos outros.
		</p>
		
		<p> Caracterizadas por suas ações de solidariedade no campo das políticas públicas e pelo legítimo exercício de pressões políticas em proveito de populações 
		excluídas das condições da cidadania. Existem vários tipos 
		de ONG’s e cada uma pode defender especificamente um certo direito, gênero e outros.
		</p>

		<h2 class="blog-post-title"> Agora: O que a nossa ONG faz? Qual seu nome? Seu objetivo?</h2>
	  
		<p>Está ONG, a qual nomeamos CDP (Centro de Defesa ao Pantanal), protege, especificamente, ao bioma Pantanal, como por exemplo: a vida vegetal 
		(meio ambiente), os direitos animais e os direitos indígenas, cujo foram afetados devido as queimadas, desmatamentos, conforme a IBGE estimou, sua área 
		aproximada era de 150.355 km² (IBGE,2004), ocupando assim 1,76% da área total do território brasileiro, mas entre 2000 e 2018, IBGE estimou em 2,1 mil km² a 
		área devastada no Pantanal, que era o bioma mais preservado do país. </p>
	  
		<p>Já em 2020, pesquisadores estimam a perda de pelo menos 23 mil km² consumidos pelo fogo. Por isso, está ONG tem como objetivo, proteger o Pantanal, 
		como um todo, criada em 30/09/2020.</p>
